package com.company.chapter3.operators;

public class AssignmentOperators {
    public static void tryAssignmentOperators() {

        int assignment1 = 5;
        System.out.println(assignment1);

        int assignment2 = 5;
        assignment2 += 3;
        System.out.println(assignment2);

        int assignment3 = 5;
        assignment3 -= 3;
        System.out.println(assignment3);

        int assignment4 = 5;
        assignment4 *= 3;
        System.out.println(assignment4);

        double assignment5 = 5;
        assignment5 /= 3;
        System.out.println(assignment5);

        int assignment6 = 5;
        assignment6 %= 3;
        System.out.println(assignment6);
    }
}
